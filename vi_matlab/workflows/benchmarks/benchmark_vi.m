function row = empty_row()
    row = struct( ...
        'name', '', 'map_x', 0, 'map_y', 0, 'type', '', ...
        'ref_sweeps', 0, 'ref_total_ms', 0, 'ref_per_sweep_ms', 0, ...
        'ref_final_delta', 0, 'ref_converged', false, ...
        'fpga_sweeps', 0, 'fpga_total_ms', 0, 'fpga_per_sweep_ms', 0, ...
        'fpga_final_delta', 0, 'fpga_converged', false, ...
        'fpga_value_mismatch_pct', 0, 'fpga_action_mismatch_pct', 0, ...
        'f2d_iters', 0, 'f2d_total_updates', 0, 'f2d_total_ms', 0, ...
        'f2d_per_update_us', 0, 'f2d_value_mismatch_pct', 0, ...
        'f3d_iters', 0, 'f3d_total_updates', 0, 'f3d_total_ms', 0, ...
        'f3d_per_update_us', 0, 'f3d_value_mismatch_pct', 0, ...
        'fstack_iters', 0, 'fstack_total_updates', 0, 'fstack_total_ms', 0, ...
        'fstack_per_update_us', 0, 'fstack_value_mismatch_pct', 0, ...
        'pyramid_iters', 0, 'pyramid_visited_states', 0, 'pyramid_total_ms', 0, ...
        'pyramid_per_state_us', 0, 'pyramid_value_mismatch_pct', 0);
end

function warmup(value, penalty, goal_mask, transitions, map_x, map_y)
    % One scratch run per implementation to amortize MATLAB JIT warm-up.
    vi_full_reference(value, penalty, goal_mask, transitions, map_x, map_y, 0, 1);
    v = value;
    [v, ~] = vi_sweep_stream_algo(v, v, penalty, goal_mask, transitions, map_x, map_y, 0);
    [~, ~] = vi_sweep_stream_algo(v, v, penalty, goal_mask, transitions, map_x, map_y, 1);
    vi_frontier_2d(value, penalty, goal_mask, transitions, map_x, map_y, 1);
    vi_frontier_3d(value, penalty, goal_mask, transitions, map_x, map_y, 1);
    vi_frontier_stack(value, penalty, goal_mask, transitions, map_x, map_y, 1);
    vi_pyramid_sweep(value, penalty, goal_mask, transitions, map_x, map_y, ...
        0, 1, 4, 1, 1, 1);
end

function [v, sweeps, final_delta] = run_fpga_mimic(value, penalty, goal_mask, ...
    transitions, map_x, map_y, max_sweeps)
    v = value;
    sweeps = 0;
    final_delta = inf;
    for s = 1:max_sweeps
        [v, d0] = vi_sweep_stream_algo(v, v, penalty, goal_mask, ...
            transitions, map_x, map_y, 0);
        [v, d1] = vi_sweep_stream_algo(v, v, penalty, goal_mask, ...
            transitions, map_x, map_y, 1);
        sweeps = s;
        final_delta = max(d0, d1);
        if final_delta == 0
            break;
        end
    end
end

function m = compute_bench_metrics(v_ref, v_other, a_ref, a_other, goal_mask, penalty, p)
    OB = double(p.PENALTY_OBSTACLE);
    free_mask = ~goal_mask & repmat(penalty ~= OB, [1, 1, p.N_THETA]);
    total = nnz(free_mask);

    if total == 0
        m = struct('value_mismatch_pct', 0, 'action_mismatch_pct', 0, ...
            'mean_abs_value_pct', 0, 'max_abs_value', 0);
        return;
    end

    v_ref_f   = v_ref(free_mask);
    v_other_f = v_other(free_mask);

    if ~isempty(a_other)
        a_ref_f   = a_ref(free_mask);
        a_other_f = a_other(free_mask);
        amiss = 100 * nnz(a_ref_f ~= a_other_f) / total;
    else
        amiss = 0;
    end

    denom = max(double(v_ref_f), 1);
    m = struct( ...
        'value_mismatch_pct',  100 * nnz(v_ref_f ~= v_other_f) / total, ...
        'action_mismatch_pct', amiss, ...
        'mean_abs_value_pct',  100 * mean(abs(double(v_other_f) - double(v_ref_f)) ./ denom), ...
        'max_abs_value',       max(abs(double(v_other_f) - double(v_ref_f))));
end

function flag = accuracy_flag(metrics, include_action)
    flags = {};
    if metrics.value_mismatch_pct ~= 0
        flags{end + 1} = sprintf('vmiss=%.2f%%', metrics.value_mismatch_pct);
    end
    if include_action && metrics.action_mismatch_pct ~= 0
        flags{end + 1} = sprintf('amiss=%.2f%%', metrics.action_mismatch_pct);
    end

    if isempty(flags)
        flag = '';
    else
        flag = sprintf('  ! %s', strjoin(flags, ' '));
    end
end

function print_markdown_table(rows)
    fprintf('\n');
    fprintf(['| name | size | ref_ms | fpga_ms | f2d_ms | f2d_iter | f2d_up | ' ...
             'f3d_ms | f3d_iter | f3d_up | fstack_ms | fst_iter | fst_up | ' ...
             'pyr_ms | pyr_iter | pyr_visit |\n']);
    fprintf(['|------|------|-------:|--------:|-------:|---------:|-------:|' ...
             '-------:|---------:|-------:|----------:|---------:|-------:|' ...
             '-------:|---------:|----------:|\n']);
    for k = 1:numel(rows)
        r = rows(k);
        fprintf(['| %s | %dx%d | %.1f | %.1f | %.1f | %d | %d | ' ...
                 '%.1f | %d | %d | %.1f | %d | %d | %.1f | %d | %d |\n'], ...
            r.name, r.map_x, r.map_y, ...
            r.ref_total_ms, r.fpga_total_ms, ...
            r.f2d_total_ms,    r.f2d_iters,    r.f2d_total_updates, ...
            r.f3d_total_ms,    r.f3d_iters,    r.f3d_total_updates, ...
            r.fstack_total_ms, r.fstack_iters, r.fstack_total_updates, ...
            r.pyramid_total_ms, r.pyramid_iters, r.pyramid_visited_states);
    end
end

function write_csv(rows, path)
    fid = fopen(path, 'w');
    if fid < 0
        error('benchmark_vi:csv', 'cannot open %s for writing', path);
    end
    cleanup = onCleanup(@() fclose(fid));

    field_names = fieldnames(rows(1));
    fprintf(fid, '%s\n', strjoin(field_names, ','));

    for k = 1:numel(rows)
        r = rows(k);
        parts = cell(1, numel(field_names));
        for f = 1:numel(field_names)
            v = r.(field_names{f});
            if ischar(v) || isstring(v)
                parts{f} = char(v);
            elseif islogical(v)
                parts{f} = sprintf('%d', v);
            elseif isinteger(v) || mod(v, 1) == 0
                parts{f} = sprintf('%g', v);
            else
                parts{f} = sprintf('%.6f', v);
            end
        end
        fprintf(fid, '%s\n', strjoin(parts, ','));
    end
end

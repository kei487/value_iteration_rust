function rows = benchmark_vi()
%BENCHMARK_VI Speed/accuracy comparison across VI implementations.
%
%   For each case from bench_cases() runs:
%     ref     : vi_full_reference  (oracle; existing)
%     fpga    : vi_sweep_stream_algo  (FPGA-mimic streaming kernel; existing)
%     f2d     : vi_frontier_2d        (frontier-VI, 2D spatial bitboard)
%     f3d     : vi_frontier_3d        (frontier-VI, 3D (x,y,theta) bitboard)
%     fstack  : vi_frontier_stack     (frontier-VI, 60 stacked 2D bitboards)
%     pyramid : vi_pyramid_sweep      (2x2 aggregation pyramid sweep)
%
%   Writes Markdown summary to stdout and CSV to vi_matlab/artifacts/benchmarks/results/.

    layout = vi_matlab_layout();
    setup_matlab_paths('src', 'tests');

    p = vi_params();
    max_sweeps = 200;
    max_frontier_iters = 4000;   % frontier variants typically need more outer iters
    threshold = 0;
    transitions = gen_transitions('paper_mc');

    cases = bench_cases();
    rows = repmat(empty_row(), 1, numel(cases));
    n_cases = numel(cases);
    n_impls = 6;

    fprintf('=== benchmark phase (%d cases x %d impls) ===\n', n_cases, n_impls);
    for k = 1:n_cases
        c = cases(k);
        fprintf('  [%2d/%2d] %-14s\n', k, n_cases, c.name);

        [value0, penalty, ~, ~, goal_mask] = gen_test_map( ...
            c.map_x, c.map_y, c.type, c.opts);

        warmup(value0, penalty, goal_mask, transitions, c.map_x, c.map_y);

        % --- Paper reference (oracle) -----------------------------------
        t0 = tic;
        [v_ref, a_ref, sw_ref, d_ref] = vi_full_reference( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, threshold, max_sweeps);
        t_ref_ms = toc(t0) * 1000;

        % --- FPGA-mimic streaming kernel --------------------------------
        t0 = tic;
        [v_fpga, sw_fpga, d_fpga] = run_fpga_mimic( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, max_sweeps);
        t_fpga_ms = toc(t0) * 1000;
        a_fpga = compute_action_table_reference(v_fpga, penalty, ...
            goal_mask, transitions, c.map_x, c.map_y);

        % --- Frontier VI variants ---------------------------------------
        t0 = tic;
        [v_f2d, it_f2d, up_f2d] = vi_frontier_2d( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, max_frontier_iters);
        t_f2d_ms = toc(t0) * 1000;

        t0 = tic;
        [v_f3d, it_f3d, up_f3d] = vi_frontier_3d( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, max_frontier_iters);
        t_f3d_ms = toc(t0) * 1000;

        t0 = tic;
        [v_fstack, it_fstack, up_fstack] = vi_frontier_stack( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, max_frontier_iters);
        t_fstack_ms = toc(t0) * 1000;

        t0 = tic;
        [v_pyramid, it_pyramid, visit_pyramid] = vi_pyramid_sweep( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, threshold, max_frontier_iters, 4, 8, ...
            max_sweeps, 1);
        t_pyramid_ms = toc(t0) * 1000;

        % --- Mismatch metrics vs oracle ---------------------------------
        m_fpga   = compute_bench_metrics(v_ref, v_fpga,   a_ref, a_fpga,   goal_mask, penalty, p);
        m_f2d    = compute_bench_metrics(v_ref, v_f2d,    a_ref, [],       goal_mask, penalty, p);
        m_f3d    = compute_bench_metrics(v_ref, v_f3d,    a_ref, [],       goal_mask, penalty, p);
        m_fstack = compute_bench_metrics(v_ref, v_fstack, a_ref, [],       goal_mask, penalty, p);
        m_pyramid = compute_bench_metrics(v_ref, v_pyramid, a_ref, [],     goal_mask, penalty, p);

        rows(k) = struct( ...
            'name',                c.name, ...
            'map_x',               c.map_x, ...
            'map_y',               c.map_y, ...
            'type',                c.type, ...
            'ref_sweeps',          sw_ref, ...
            'ref_total_ms',        t_ref_ms, ...
            'ref_per_sweep_ms',    t_ref_ms / max(sw_ref, 1), ...
            'ref_final_delta',     d_ref, ...
            'ref_converged',       d_ref <= threshold, ...
            'fpga_sweeps',         sw_fpga, ...
            'fpga_total_ms',       t_fpga_ms, ...
            'fpga_per_sweep_ms',   t_fpga_ms / max(sw_fpga, 1), ...
            'fpga_final_delta',    d_fpga, ...
            'fpga_converged',      d_fpga <= threshold, ...
            'fpga_value_mismatch_pct',  m_fpga.value_mismatch_pct, ...
            'fpga_action_mismatch_pct', m_fpga.action_mismatch_pct, ...
            'f2d_iters',                it_f2d, ...
            'f2d_total_updates',        up_f2d, ...
            'f2d_total_ms',             t_f2d_ms, ...
            'f2d_per_update_us',        1000 * t_f2d_ms / max(up_f2d, 1), ...
            'f2d_value_mismatch_pct',   m_f2d.value_mismatch_pct, ...
            'f3d_iters',                it_f3d, ...
            'f3d_total_updates',        up_f3d, ...
            'f3d_total_ms',             t_f3d_ms, ...
            'f3d_per_update_us',        1000 * t_f3d_ms / max(up_f3d, 1), ...
            'f3d_value_mismatch_pct',   m_f3d.value_mismatch_pct, ...
            'fstack_iters',             it_fstack, ...
            'fstack_total_updates',     up_fstack, ...
            'fstack_total_ms',          t_fstack_ms, ...
            'fstack_per_update_us',     1000 * t_fstack_ms / max(up_fstack, 1), ...
            'fstack_value_mismatch_pct', m_fstack.value_mismatch_pct, ...
            'pyramid_iters',              it_pyramid, ...
            'pyramid_visited_states',     visit_pyramid, ...
            'pyramid_total_ms',           t_pyramid_ms, ...
            'pyramid_per_state_us',       1000 * t_pyramid_ms / max(visit_pyramid, 1), ...
            'pyramid_value_mismatch_pct', m_pyramid.value_mismatch_pct);

        fprintf('    %-15s M=%7.1fms (%4d sw)\n', ...
            'reference', t_ref_ms, sw_ref);
        fprintf('    %-15s M=%7.1fms (%4d sw)%s\n', ...
            'fpga_mimic', t_fpga_ms, sw_fpga, accuracy_flag(m_fpga, true));
        fprintf('    %-15s M=%7.1fms (%4d it)%s\n', ...
            'frontier_2d', t_f2d_ms, it_f2d, accuracy_flag(m_f2d, false));
        fprintf('    %-15s M=%7.1fms (%4d it)%s\n', ...
            'frontier_3d', t_f3d_ms, it_f3d, accuracy_flag(m_f3d, false));
        fprintf('    %-15s M=%7.1fms (%4d it)%s\n', ...
            'frontier_stack', t_fstack_ms, it_fstack, accuracy_flag(m_fstack, false));
        fprintf('    %-15s M=%7.1fms (%4d it)%s\n', ...
            'pyramid_sweep', t_pyramid_ms, it_pyramid, accuracy_flag(m_pyramid, false));

        % Regression guard: exact variants must be bit-exact to oracle.
        for tag = {'f2d', 'f3d', 'fstack', 'pyramid'}
            field = sprintf('%s_value_mismatch_pct', tag{1});
            if rows(k).(field) ~= 0
                warning('benchmark_vi:mismatch', ...
                    '%s mismatch on case %s: %.4f%%', tag{1}, c.name, rows(k).(field));
            end
        end
    end

    print_markdown_table(rows);

    results_dir = layout.artifacts_benchmarks_results;
    if ~exist(results_dir, 'dir'); mkdir(results_dir); end
    csv_path = fullfile(results_dir, ...
        sprintf('benchmark_%s.csv', datestr(now, 'yyyymmdd_HHMMSS'))); %#ok<TNOW1,DATST>
    write_csv(rows, csv_path);
    fprintf('\nCSV written to %s\n', csv_path);
end

